## the function looks if the inverse of matrix x exists. 
## if yes, it returns the cached inverse
## if no, it calculates the inverse and stores the outcome in the cache & returns the inverse

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getinv()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinv(m)
  m
}
